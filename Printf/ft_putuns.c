#include "ft_printf.h"

int	ft_putuns(long int nbr)
{
	int	i;

	i = 0;
	if (nbr >= 10)
	{
		i++;
		ft_putuns((nbr / 10));
	}
	ft_putchar((nbr % 10 + '0'));
	return (i);
}
