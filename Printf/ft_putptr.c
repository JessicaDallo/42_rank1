#include "ft_printf.h"

int	ft_putptr(unsigned long ptr)
{
	int	i;

	i = ft_count_nbr(ptr);
	ft_putstr("0x");
	if (ptr >= 16)
	{
		ft_putptr(ptr / 16);
		ft_putptr(ptr % 16);
	}
	else
	{
		if (ptr <= 9)
			ft_putchar(ptr + 48);
		else
			ft_putchar(ptr - 10 + 'a');
	}
	return (i + 2);
}
